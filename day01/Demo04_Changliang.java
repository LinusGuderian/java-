public class Demo04_Changliang {
    public static void main(String[] args) {
        /*
        常量:
        1.定义：在程序运行过程中，其值不可以发生改变的量

        2.常量的分类：
            1.字符串常量 ：“HelloWorld” ，“拉登是我干掉的”
            2.整数常量：666,-88;
            3.小数常量：3.14，-5.14;
            4.字符常量： ‘A’,‘0’,'我';
            5.布尔常量：true,false;
            6.空常量
         */

        //字符串常量
        System.out.println("HelloWorld");
        System.out.println("拉登是我干掉的");
        System.out.println("============================");

        //整数常量
        System.out.println(666);
        System.out.println(888);
        System.out.println("============================");

        //小数常量
        System.out.println(3.14);
        System.out.println(-5.14);
        System.out.println("============================");

        //字符常量
        System.out.println('A');
        System.out.println('0');
        System.out.println('我');
        System.out.println("============================");

        //布尔常量
        System.out.println(true);
        System.out.println(false);
        System.out.println("============================");

        //空常量
        //空常量不是拿来输出的
//        System.out.println(null);
        System.out.println("============================");




    }
}
