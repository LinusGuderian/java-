public class Demo06_Bianliang {
    public static void main(String[] args) {
        /**
         * 变量
         *  一：定义：
         *      在程序运行过程中，其值可以发生改变的量；
         *      从本质上讲，变量是内存中的一小块区域
         *  二、格式
         *      格式： 数据类型变量名 = 变量名；
         *      eg ：int a   =    10;
         *
         *  三、 变量的使用
         *      变量的使用：取值和修改值
         *      eg ：a ;
         *
         *      修改值格式：变量名 = 变量值;
         *      eg: a = 20 ;
         *
         *
         *
         *
         */
        //定义变量
        int a   =   10;
        //输出变量
        System.out.println(a);

        //修改变量
        a= 20;
        System.out.println(a);

    }
}
